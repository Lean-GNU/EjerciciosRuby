=begin
Inicializar un String con la cadena "mAriA" luego modificar el contenido de la misma variable llamando
a los métodos upcase!, downcase! y capitalize!.
Es importante tener en cuenta que en el lenguaje Ruby se puede utilizar los caracteres ! y ? al final del nombre de un método.
=end

name = "mAriA"
name.upcase!
puts name
name.downcase!
puts name
name.capitalize!
puts name
